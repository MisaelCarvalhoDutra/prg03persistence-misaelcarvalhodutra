    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
      
    // Pega os dados da linha selecionada
    int linhaSelecionada = jTable1.getSelectedRow(); 

    // Verifica se uma linha foi selecionada
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, 
            "Selecione um curso para editar!", 
            "Aviso", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Agora, pega o ID real da linha selecionada (coluna 2)
    Long idCurso = Long.valueOf(jTable1.getValueAt(linhaSelecionada, 2).toString()); // Acessando a coluna oculta para o ID

    // Busca o curso no banco pelo ID
    Curso curso = controller.findById(idCurso);
    
    

    // Verifica se o curso foi encontrado
    if (curso == null) {
        JOptionPane.showMessageDialog(this, 
            "Curso não encontrado no banco de dados!", 
            "Erro", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Abre a tela de edição com os dados verdadeiros do banco
    CursoEditar tela = new CursoEditar(
        this,
        curso.getId(),
        curso.getNome(),
        curso.getCodigo()
    );

    tela.setVisible(true);
    tela.setLocationRelativeTo(null);
    }//GEN-LAST:event_btnEditarActionPerformed

