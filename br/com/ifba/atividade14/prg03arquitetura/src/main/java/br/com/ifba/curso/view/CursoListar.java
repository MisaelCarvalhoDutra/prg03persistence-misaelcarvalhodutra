    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
    int linhaSelecionada = jTable1.getSelectedRow();

    if (linhaSelecionada == -1) {
    JOptionPane.showMessageDialog(this,
        "Selecione um curso para excluir!",
        "Aviso",
        JOptionPane.WARNING_MESSAGE);
    return;
    }

    // Agora usamos o ID oculto da tabela
    Long id = Long.valueOf(jTable1.getValueAt(linhaSelecionada, 2).toString());

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Excluir este curso?",
        "Confirmar",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm == JOptionPane.YES_OPTION) {

    // buscar o curso pelo ID
    Curso curso = controller.findById(id);

    // excluir usando o método que já existe no controller
    controller.delete(curso);
    
    carregarCursos();

    JOptionPane.showMessageDialog(this, "Curso excluído!");
}


    }//GEN-LAST:event_btnExcluirActionPerformed

